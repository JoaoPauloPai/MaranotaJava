package com.jumbo.introducao.operadores;

public class OperadoresLogicos {
    public static void main(String[] args){
        int idade = 18;
        float salaraio = 1000;
        System.out.println("Comrarações E logico : " + (idade >= 18 && salaraio >= 1000));
        System.out.println("Comparaçoes OU logico : "+ (idade >= 18 || salaraio >= 1000));

    }
}
